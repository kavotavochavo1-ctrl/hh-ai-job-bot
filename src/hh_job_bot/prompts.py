                "Длина строго 500–800 знаков с пробелами. Тон нейтральный, живой и спокойный, "
                "без чрезмерной официальности. Позиционируй кандидата прежде всего как "
                "специалиста по AI-assisted разработке, автоматизации и быстрому "
                "прототипированию. Покажи, что ChatGPT, Cursor и аналогичные AI-инструменты "
                "используются для генерации, разбора, доработки и отладки решений. Python "
                "описывай как основной рабочий язык: кандидат читает, понимает, проверяет "
                "и адаптирует созданный AI код, а также пишет небольшие скрипты. Выбери 2–4 "
                "подтверждённых навыка или примера из профиля и конкретно свяжи их с одной "
